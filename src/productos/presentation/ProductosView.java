    private void buscartipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscartipoActionPerformed
        if (!validar_tipo()) {
            try {
                controller.buscar(this.toProductosearch());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "No se encontro ninguna coincidencia", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buscartipoActionPerformed

    private void btnAgegarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgegarActionPerformed
        if (!validar()) {
            try {
                controller.agregar(this.toProducto());
                limpiar_agregar();
                JOptionPane.showMessageDialog(this, "Datos registrados", "OK", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "error al ingresar", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAgegarActionPerformed


