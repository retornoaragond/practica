    @Override
    public void update(Observable o, Object arg) {
        this.limpiarErrores();
        Producto filtro = model.getFilter();
        this.productostable.setModel(model.getProductos());
        this.CBagregar.setModel(model.getCombotiposask());
        this.CBtiposbuscar.setModel(model.getCombotiposadd());
        this.fromProducto(filtro);
        this.CBagregar.setSelectedItem(0);
        this.CBtiposbuscar.setSelectedItem(0);
    }

    public void fromProducto(Producto actual) {
        if (!actual.getTipo().getIdTipo().equals("")) {
            this.CBtiposbuscar.setSelectedItem(actual.getTipo());
        } else {
            this.CBtiposbuscar.setSelectedIndex(0);
        }

        this.nombrebuscar.setText(actual.getNombre());
    }
