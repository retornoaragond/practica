    Producto toProducto() {
        Producto result = new Producto();
        result.setCodigo(this.codigotxt.getText());
        result.setNombre(this.nombretxt.getText());
        result.setImportado(this.cbimportado.isSelected());
        result.setPrecio(Integer.parseInt(this.preciotxt.getText()));
        result.setTipo((Tipo) this.CBagregar.getSelectedItem());
        return result;
    }

    Producto toProductosearch() {
        Producto result = new Producto();
        result.setNombre(this.nombrebuscar.getText());
        result.setTipo((Tipo) this.CBtiposbuscar.getSelectedItem());
        return result;
    }

    public void limpiarErrores() {
        this.lcodigo.setForeground(Productos.COLOR_OK);
        this.lnombre.setForeground(Productos.COLOR_OK);
        this.lprecio.setForeground(Productos.COLOR_OK);
        this.ltipo.setForeground(Productos.COLOR_OK);
        this.lnombreB.setForeground(Productos.COLOR_OK);
        this.ltipobuscar.setForeground(Productos.COLOR_OK);
    }

    boolean validar() {
        boolean error = false;
        this.lcodigo.setForeground(Productos.COLOR_OK);
        if (this.codigotxt.getText().isEmpty()) {
            this.lcodigo.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        this.lnombre.setForeground(Productos.COLOR_OK);
        if (this.nombretxt.getText().isEmpty()) {
            this.lnombre.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        this.lprecio.setForeground(Productos.COLOR_OK);
        if (this.preciotxt.getText().isEmpty()) {
            this.lprecio.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        this.ltipo.setForeground(Productos.COLOR_OK);
        if (this.CBagregar.getSelectedIndex() == 0) {
            this.ltipo.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        return error;
    }

    boolean validar_nombre() {
        boolean error = false;
        this.lnombreB.setForeground(Productos.COLOR_OK);
        if (this.nombrebuscar.getText().isEmpty()) {
            this.lnombreB.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        return error;
    }

    boolean validar_tipo() {
        boolean error = false;
        this.ltipobuscar.setForeground(Productos.COLOR_OK);
        if (this.CBtiposbuscar.getSelectedIndex() == 0) {
            this.ltipobuscar.setForeground(Productos.COLOR_ERROR);
            error = true;
        }
        return error;
    }

    public void limpiar_agregar() {
        this.codigotxt.setText("");
        this.nombretxt.setText("");
        this.preciotxt.setText("");
        this.CBagregar.setSelectedIndex(0);
        this.cbimportado.setSelected(false);
    }

